import { createClient } from "@/lib/supabase/server"
import { NextResponse } from "next/server"

export async function POST(request: Request, { params }: { params: Promise<{ id: string }> }) {
  try {
    const { id } = await params
    const supabase = await createClient()

    const { questions, logicModel } = await request.json()

    console.log("[v0] Generating indicators with OpenAI...")
    console.log("[v0] Number of questions:", questions.length)

    const response = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
      },
      body: JSON.stringify({
        model: "gpt-4o",
        messages: [
          {
            role: "system",
            content:
              "You are an expert in program evaluation and indicator development. Generate specific, measurable indicators for evaluation questions. Always return valid JSON.",
          },
          {
            role: "user",
            content: `Based on the following evaluation questions, generate 3-5 specific, measurable indicators for each question.

EVALUATION QUESTIONS:
${questions.map((q: any, idx: number) => `${idx + 1}. [ID: ${q.id}] ${q.question}`).join("\n")}

LOGIC MODEL SUMMARY:
${logicModel?.nodes ? `- ${logicModel.nodes.length} program elements across inputs, activities, outputs, outcomes, and impacts` : "No logic model available"}

Generate indicators that are:
1. Specific and measurable
2. Directly related to the evaluation questions
3. Feasible to collect
4. Mix of quantitative and qualitative indicators
5. Cover different aspects of the program

Return a JSON object with an "indicators" array. Each indicator must have:
- question_id: Use the actual UUID from the questions above
- indicator_text: The specific indicator description

Format:
{
  "indicators": [
    {"question_id": "actual-uuid", "indicator_text": "description"},
    {"question_id": "actual-uuid", "indicator_text": "description"}
  ]
}

Generate 3-5 indicators per question.`,
          },
        ],
        response_format: { type: "json_object" },
        max_tokens: 3000,
        temperature: 0.7,
      }),
    })

    if (!response.ok) {
      const errorText = await response.text()
      console.error("[v0] OpenAI API error:", errorText)
      throw new Error(`OpenAI API error: ${response.status} ${errorText}`)
    }

    const data = await response.json()
    console.log("[v0] OpenAI response received")

    const content = data.choices[0].message.content
    console.log("[v0] Raw AI response:", content.substring(0, 200))

    let parsedResponse
    try {
      parsedResponse = JSON.parse(content)
      console.log("[v0] Full parsed response:", JSON.stringify(parsedResponse, null, 2))
    } catch (parseError) {
      console.error("[v0] Failed to parse AI response:", content)
      throw new Error("Invalid JSON response from AI")
    }

    let generatedIndicators = []

    if (Array.isArray(parsedResponse)) {
      generatedIndicators = parsedResponse
    } else if (parsedResponse.indicators && Array.isArray(parsedResponse.indicators)) {
      generatedIndicators = parsedResponse.indicators
    } else if (parsedResponse.data && Array.isArray(parsedResponse.data)) {
      generatedIndicators = parsedResponse.data
    } else if (parsedResponse.items && Array.isArray(parsedResponse.items)) {
      generatedIndicators = parsedResponse.items
    } else {
      // Try to find any array property in the response
      const arrayProp = Object.values(parsedResponse).find((val) => Array.isArray(val))
      if (arrayProp) {
        generatedIndicators = arrayProp as any[]
      }
    }

    console.log("[v0] Parsed indicators count:", generatedIndicators.length)

    if (!generatedIndicators || generatedIndicators.length === 0) {
      console.error("[v0] No indicators found in response. Response structure:", Object.keys(parsedResponse))
      throw new Error("No indicators generated by AI")
    }

    const validQuestionIds = new Set(questions.map((q: any) => q.id))
    const indicatorsToInsert = generatedIndicators
      .filter((indicator: any) => {
        if (!validQuestionIds.has(indicator.question_id)) {
          console.warn("[v0] Skipping indicator with invalid question_id:", indicator.question_id)
          return false
        }
        return true
      })
      .map((indicator: any) => ({
        program_id: id,
        question_id: indicator.question_id,
        indicator_text: indicator.indicator_text,
        is_ai_generated: true,
        is_selected: false,
      }))

    console.log("[v0] Valid indicators to insert:", indicatorsToInsert.length)

    if (indicatorsToInsert.length === 0) {
      throw new Error("No valid indicators to insert (all question_ids were invalid)")
    }

    const { data: insertedIndicators, error } = await supabase.from("indicators").insert(indicatorsToInsert).select()

    if (error) {
      console.error("[v0] Database error:", error)
      throw error
    }

    console.log("[v0] Successfully inserted indicators:", insertedIndicators.length)

    return NextResponse.json({ indicators: insertedIndicators })
  } catch (error) {
    console.error("[v0] Error generating indicators:", error)
    return NextResponse.json(
      { error: error instanceof Error ? error.message : "Failed to generate indicators" },
      { status: 500 },
    )
  }
}
