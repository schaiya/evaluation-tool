import { createClient } from "@/lib/supabase/server"
import { NextResponse } from "next/server"

export async function POST(request: Request, { params }: { params: Promise<{ id: string }> }) {
  try {
    const { id } = await params
    const supabase = await createClient()

    const { analysisResults, questions } = await request.json()

    // Fetch program details
    const { data: program } = await supabase.from("programs").select("*").eq("id", id).single()

    const findingsWithVisuals = analysisResults.flatMap(
      (result: any) =>
        result.results.findings?.map((f: any) => ({
          ...f,
          hasVisualization: !!f.visualization,
          visualizationType: f.visualization?.type,
        })) || [],
    )

    const openaiResponse = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
      },
      body: JSON.stringify({
        model: "gpt-4o",
        messages: [
          {
            role: "system",
            content:
              "You are an expert evaluation report writer. Create comprehensive, professional evaluation reports based on analysis results with visualizations.",
          },
          {
            role: "user",
            content: `Create a comprehensive narrative evaluation report based on the analysis results.

Program: ${program?.name}
Description: ${program?.description}

Evaluation Questions:
${questions.map((q: any, i: number) => `${i + 1}. ${q.question_text}`).join("\n")}

Analysis Results (includes ${findingsWithVisuals.filter((f: any) => f.hasVisualization).length} findings with data visualizations):
${JSON.stringify(analysisResults, null, 2)}

Write a professional evaluation report with the following sections:
1. Executive Summary (2-3 paragraphs)
2. Introduction (program background and evaluation purpose)
3. Methodology (brief overview of data collection and analysis)
4. Findings (organized by evaluation question, with supporting evidence - note where visualizations are available)
5. Conclusions (synthesis of key findings)
6. Recommendations (actionable suggestions based on findings)

When referencing findings with visualizations, mention that data charts support these findings.
Write in clear, professional language. Use specific evidence from the analysis results. Make the report comprehensive but readable. Format with clear section headings.

Return only the report text, properly formatted with line breaks between sections.`,
          },
        ],
        temperature: 0.7,
        max_tokens: 4000,
      }),
    })

    if (!openaiResponse.ok) {
      const errorData = await openaiResponse.json()
      console.error("[v0] OpenAI API error:", errorData)
      throw new Error(`OpenAI API error: ${JSON.stringify(errorData)}`)
    }

    const openaiData = await openaiResponse.json()
    console.log("[v0] OpenAI response received")

    const reportText = openaiData.choices[0]?.message?.content

    if (!reportText) {
      throw new Error("No report content generated by AI")
    }

    console.log("[v0] Report generated, length:", reportText.length)

    // Check if report exists
    const { data: existing } = await supabase.from("reports").select("id").eq("program_id", id).maybeSingle()

    let report
    if (existing) {
      const { data, error } = await supabase
        .from("reports")
        .update({
          report_content: reportText,
          updated_at: new Date().toISOString(),
        })
        .eq("program_id", id)
        .select()
        .single()

      if (error) throw error
      report = data
    } else {
      const { data, error } = await supabase
        .from("reports")
        .insert({
          program_id: id,
          report_content: reportText,
        })
        .select()
        .single()

      if (error) throw error
      report = data
    }

    console.log("[v0] Report saved to database")

    return NextResponse.json({ report })
  } catch (error) {
    console.error("[v0] Error generating report:", error)
    return NextResponse.json({ error: "Failed to generate report" }, { status: 500 })
  }
}
